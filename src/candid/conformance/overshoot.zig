const lib = @import("lib.zig");
const testing = lib.testing;
const decode = lib.decode;
const Principal = lib.Principal;
const expectDecodeError = lib.expectDecodeError;
const expectDecodeManyError = lib.expectDecodeManyError;

test "overshoot: type table length" {
    // LEB128 length 0x80_94_eb_dc_03 = 1_000_000_000 type entries
    try expectDecodeManyError(
        struct {},
        error.EndOfStream,
        "DIDL" ++ &[_]u8{ 0x80, 0x94, 0xeb, 0xdc, 0x03, 0x00 },
    );
}

